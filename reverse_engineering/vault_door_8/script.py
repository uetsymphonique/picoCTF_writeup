arr = [0xF4, 0xC0, 0x97, 0xF0, 0x77, 0x97, 0xC0, 0xE4,
       0xF0, 0x77, 0xA4, 0xD0, 0xC5, 0x77, 0xF4, 0x86,
       0xD0, 0xA5, 0x45, 0x96, 0x27, 0xB5, 0x77, 0xD2,
       0xD0, 0xB4, 0xE1, 0xC1, 0xE0, 0xD0, 0xD0, 0xE0]


def recover_bits(binary_string):
    return binary_string[4:6] + binary_string[0:2] + binary_string[6:8] + binary_string[2:4]


ans = 'picoCTF{'

for item in arr:
    binary_string = bin(item)[2:].rjust(8, '0')
    ans += chr(int(recover_bits(binary_string), 2))

print(ans + '}')
